function formatRuleNumber(index) {
  return String(index + 1).padStart(2, "0");
}

function createRules() {
  if (!rulesContainer) return;

  if (!SERVER_RULES.length) {
    rulesContainer.innerHTML =
      '<div class="rule-empty">لا توجد قوانين مضافة حالياً.</div>';
    return;
  }

  rulesContainer.innerHTML = SERVER_RULES.map((rule, index) => `
    <article class="rule-item">
      <button
        class="rule-question"
        type="button"
        aria-expanded="false"
        aria-controls="rule-answer-${index}"
      >
        <span class="rule-number">${formatRuleNumber(index)}</span>
        <span class="rule-title">${rule.title}</span>
        <span class="rule-chevron" aria-hidden="true">⌄</span>
      </button>

      <div class="rule-answer" id="rule-answer-${index}">
        <div class="rule-answer-inner">
          <div class="rule-answer-content">${rule.description}</div>
        </div>
      </div>
    </article>
  `).join("");

  document.querySelectorAll(".rule-question").forEach(button => {
    button.addEventListener("click", () => {
      const item = button.closest(".rule-item");
      const isOpen = item.classList.toggle("open");
      button.setAttribute("aria-expanded", String(isOpen));
      updateToggleAllText();
    });
  });
}

function updateToggleAllText() {
  const items = [...document.querySelectorAll(".rule-item")];
  if (!items.length || !toggleAllButton) return;

  const allOpen = items.every(item => item.classList.contains("open"));
  toggleAllButton.textContent = allOpen ? "إغلاق الكل" : "فتح الكل";
}

toggleAllButton?.addEventListener("click", () => {
  const items = [...document.querySelectorAll(".rule-item")];
  const shouldOpen = !items.every(item => item.classList.contains("open"));

  items.forEach(item => {
    item.classList.toggle("open", shouldOpen);
    item.querySelector(".rule-question")
      ?.setAttribute("aria-expanded", String(shouldOpen));
  });

  updateToggleAllText();
});

